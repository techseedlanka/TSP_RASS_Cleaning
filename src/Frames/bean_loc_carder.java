public class bean_loc_carder {

    private String locName;
    private String locCode;
    private String shiftType;
    private String rank;
    private String guards;

    /**
     * @return the locName
     */
    public String getLocName() {
        return locName;
    }

    /**
     * @param locName the locName to set
     */
    public void setLocName(String locName) {
        this.locName = locName;
    }

    /**
     * @return the locCode
     */
    public String getLocCode() {
        return locCode;
    }

    /**
     * @param locCode the locCode to set
     */
    public void setLocCode(String locCode) {
        this.locCode = locCode;
    }

    /**
     * @return the shiftType
     */
    public String getShiftType() {
        return shiftType;
    }

    /**
     * @param shiftType the shiftType to set
     */
    public void setShiftType(String shiftType) {
        this.shiftType = shiftType;
    }

    /**
     * @return the rank
     */
    public String getRank() {
        return rank;
    }

    /**
     * @param rank the rank to set
     */
    public void setRank(String rank) {
        this.rank = rank;
    }

    /**
     * @return the guards
     */
    public String getGuards() {
        return guards;
    }

    /**
     * @param guards the guards to set
     */
    public void setGuards(String guards) {
        this.guards = guards;
    }

}
