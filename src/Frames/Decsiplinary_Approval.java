    private void find_reference() {

        String empID = txt_empid.getText();
        String refID;

        try {

            Statement st = DbConnection.getconnection().createStatement();
            ResultSet rs = st.executeQuery("select count(EPFno) from emp_discipline");
            while (rs.next()) {

                int count = Integer.parseInt(rs.getString("count(EPFno)"));

                if (count == 0) {

                    refID = "1";
                } else {

                    refID = Integer.toString((count + 1));

                }

                txt_ref.setText(empID + "/" + refID);

            }

        } catch (Exception e) {
        }
    }


