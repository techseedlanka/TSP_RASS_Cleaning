    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            Connection con = DbConnection.getconnection();
            PreparedStatement pst = con.prepareStatement("select * from employee_reg where isResigned='0' order by DefLocation");
            ResultSet rs = pst.executeQuery();

            while (rs.next()) {

                String Emp = rs.getString("EmployeeNo");
                String name = rs.getString("NameWithInitials");
                String rank = rs.getString("Designation");
                String loc = rs.getString("DefLocation");
                String gross = rs.getString("GrossSalary");
                String maxduty = rs.getString("ShiftRate");
                String extraallowed = rs.getString("ExtraShiftAllowed");

                String loc_name = "0";
                String days = "0";
                String dayRate = "";
                String nightRate = "";
                String ot = "";
                String Sunday = "";
                String Poyaday = "";
                String MinDayRate = "";
                String MaxShiftsPerMonth = "";
                String AttnAllow = "";
                String MaxShiftsAA = "";
                String ExtraShiftRate = "";
                String bra = "";
                String bradays = "";
                PreparedStatement pst1 = con.prepareStatement("select * from location_reg where LocCode='" + loc + "'");
                ResultSet rs1 = pst1.executeQuery();
                while (rs1.next()) {

                    loc_name = rs1.getString("LocName");

                }

                PreparedStatement pst2 = con.prepareStatement("select * from salary_rates where LocCode='" + loc + "' and RankCode='" + rank + "'");
                ResultSet rs2 = pst2.executeQuery();
                while (rs2.next()) {

                    dayRate = rs2.getString("DayRate");
                    nightRate = rs2.getString("NightRate");
                    ot = rs2.getString("OTRate");
                    Sunday = rs2.getString("Sunday");
                    Poyaday = rs2.getString("Poyaday");
                    MinDayRate = rs2.getString("MinDayRate");
                    MaxShiftsPerMonth = rs2.getString("MaxShiftsPerMonth");
                    AttnAllow = rs2.getString("AttnAllow");
                    MaxShiftsAA = rs2.getString("MaxShiftsAA");
                    ExtraShiftRate = rs2.getString("ExtraShiftRate");
                    bra = rs2.getString("BRAPerDay");
                    bradays = rs2.getString("MaxShiftsForBRA");

                }

                bean_salary_rates bds = new bean_salary_rates();
                bds.setEmp_code(Emp);
                bds.setRank(rank);
                bds.setEmp_name(name);
                bds.setGross(gross);
                bds.setMaxforgross(maxduty);
                bds.setExtraAllow(extraallowed);
                bds.setLoc_code(loc);
                bds.setLoc_name(loc_name);
                bds.setWorkingdays(days);
                bds.setDayrate(dayRate);
                bds.setNightRate(nightRate);
                bds.setOt(ot);
                bds.setSunday(Sunday);
                bds.setPoyaday(Poyaday);
                bds.setBasic(MinDayRate);
                bds.setMaxforBasic(MaxShiftsPerMonth);
                bds.setAa(AttnAllow);
                bds.setAamaxduty(MaxShiftsAA);
                //bds.setGross(AttnAllow);
                bds.setExtrarate(ExtraShiftRate);
                bds.setBra(bra);
                bds.setBradays(bradays);

                adv.add(bds);

            }

            print();
            adv.clear();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    void print() {
        try {
            JRBeanCollectionDataSource bcds = new JRBeanCollectionDataSource(adv);
            String path = "Reports\\Salary_rates_All.jrxml";
            JasperReport jr = JasperCompileManager.compileReport(path);
            JasperPrint jp = JasperFillManager.fillReport(jr, null, bcds);
            JasperViewer.viewReport(jp, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

