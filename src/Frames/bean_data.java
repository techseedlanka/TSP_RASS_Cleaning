public class bean_data {

    private String bean_epf;
    private String bean_Name;
    private String bean_incident;
    private String bean_date;

    /**
     * @return the bean_epf
     */
    public String getBean_epf() {
        return bean_epf;
    }

    /**
     * @param bean_epf the bean_epf to set
     */
    public void setBean_epf(String bean_epf) {
        this.bean_epf = bean_epf;
    }

    /**
     * @return the bean_Name
     */
    public String getBean_Name() {
        return bean_Name;
    }

    /**
     * @param bean_Name the bean_Name to set
     */
    public void setBean_Name(String bean_Name) {
        this.bean_Name = bean_Name;
    }

    /**
     * @return the bean_incident
     */
    public String getBean_incident() {
        return bean_incident;
    }

    /**
     * @param bean_incident the bean_incident to set
     */
    public void setBean_incident(String bean_incident) {
        this.bean_incident = bean_incident;
    }

    /**
     * @return the bean_date
     */
    public String getBean_date() {
        return bean_date;
    }

    /**
     * @param bean_date the bean_date to set
     */
    public void setBean_date(String bean_date) {
        this.bean_date = bean_date;
    }

}
