    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            String status = cmb_loanStatus.getSelectedItem().toString().toLowerCase();
            String type = cmb_deduction_type.getSelectedItem().toString();

            String sql = "SELECT * FROM salary_deductions_summery  join employee_reg on EMPno=EmployeeNo where Status='" + status + "' and Type='" + type + "' ";

            Connection conn = (Connection) DbConnection.getconnection();

            JasperDesign jd = JRXmlLoader.load("Reports\\AllEmpLoanDetail.jrxml");

            JRDesignQuery newQuery = new JRDesignQuery();
            newQuery.setText(sql);
            jd.setQuery(newQuery);

            JasperReport jr = JasperCompileManager.compileReport(jd);
            JasperPrint jp = JasperFillManager.fillReport(jr, null, conn);
            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        /*
        1. get selected month's loan duction emp nos from salary deduction table & add into array list
        2. set all on going deductions to settled in selected month >>>> in salary deduction table
        3. iterate through arraylist and check all settled deductions group by ref no. and type=loan for each employee
        4. if all settled count >0 >>> then update deduction summery table status to settled in relevent loan refno 
           
         */
        try {

            ArrayList<String> empno_list = new ArrayList<>();

            Connection con = DbConnection.getconnection();
            PreparedStatement pst = con.prepareStatement("select * from salary_deductions where Month='March' and Year='2021' and category='loan' ");
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {

                String emp = rs.getString(1);
                empno_list.add(emp);
                System.out.println("empno " + emp);

            }

            // For Loop for iterating ArrayList
            for (int i = 0; i < empno_list.size(); i++) {

                String empno_forcheck = empno_list.get(i);

                PreparedStatement pst1 = con.prepareStatement("select *,COUNT(*) from salary_deductions where EMPno='" + empno_forcheck + "'  AND STATUS LIKE '%settled%' and category='loan' group by RefNo  ");
                ResultSet rs1 = pst1.executeQuery();
                while (rs1.next()) {

                    int count = rs1.getInt("COUNT(*)");
                    if (count > 0) {

                        String refno = rs1.getString("RefNo");
                        System.out.println("RefNo " + refno);
                        PreparedStatement pst2 = con.prepareStatement("update   salary_deductions_summery set Status='settledbySAPU' where EMPno='" + empno_forcheck + "' and RefNo='" + refno + "'  ");
                        pst2.executeUpdate();
                    }

                }

            }

            JOptionPane.showMessageDialog(rootPane, "DONE");

        } catch (Exception e) {
            e.printStackTrace();
        }


    }//GEN-LAST:event_jButton2ActionPerformed

