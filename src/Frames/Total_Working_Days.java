    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        
        Thread hilo = new Thread(new Runnable() {
            
            @Override
            public void run() {
                
                try {
                    
                    Connection con = DbConnection.getconnection();
                    
                    PreparedStatement pst1 = con.prepareStatement("delete from working_days where Month='" + cmb_month.getSelectedItem().toString() + "' and Year='" + cmb_year.getSelectedItem().toString() + "'");
                    pst1.execute();
                    
                    String sql = "insert into working_days values (?,?,?,?) ";
                    PreparedStatement pst = con.prepareStatement(sql);
                    
                    int nrow = jTable1.getModel().getRowCount();
                    jProgressBar1.setMaximum(nrow);
                    jProgressBar1.setString("Reading Table...");
                    
                    if (nrow == 0) {
                        
                    } else {
                        for (int i = 0; nrow > i; i++) {
                            jProgressBar1.setValue(i);
                            jProgressBar1.setString("Preparing Data...");
                            
                            String loc = jTable1.getModel().getValueAt(i, 0).toString();
                            String month = jTable1.getModel().getValueAt(i, 2).toString();
                            String year = jTable1.getModel().getValueAt(i, 3).toString();
                            
                            String days = null;
                            if (jTable1.getModel().getValueAt(i, 4) == null) {
                                days = "31";
                            } else {
                                days = jTable1.getModel().getValueAt(i, 4).toString();
                            }
                            
                            pst.setString(1, loc);//empid
                            pst.setString(2, month);//name
                            pst.setString(3, year);//rank
                            pst.setString(4, days);//date

                            pst.addBatch();
                            
                        }
                        jProgressBar1.setMaximum(100);
                        jProgressBar1.setValue(75);
                        jProgressBar1.setString("Saving Data...");
                        
                        pst.executeBatch();
                        
                        jProgressBar1.setValue(100);
                        jProgressBar1.setString("Completed...!");
                        JOptionPane.showMessageDialog(rootPane, "Working Days Saved...!");
                        
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        });
        hilo.start();
        

    }//GEN-LAST:event_jButton14ActionPerformed

