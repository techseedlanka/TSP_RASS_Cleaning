    private void get_saved_days() {
        
        try {
            
            Connection con = DbConnection.getconnection();
            
            PreparedStatement pst = con.prepareStatement("select * from working_days join location_reg on working_days.Loc=location_reg.LocCode where working_days.Month='" + cmb_month.getSelectedItem().toString() + "' and working_days.Year='" + cmb_year.getSelectedItem().toString() + "'");
            ResultSet rs = pst.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            
            while (rs.next()) {
                
                Vector v = new Vector();
                v.add(rs.getString("LocCode"));
                v.add(rs.getString("LocName"));
                v.add(cmb_month.getSelectedItem().toString());
                v.add(cmb_year.getSelectedItem().toString());
                v.add(rs.getString("Days"));
                dtm.addRow(v);
            }
            cmb_month.setEnabled(false);
            cmb_year.setEnabled(false);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

