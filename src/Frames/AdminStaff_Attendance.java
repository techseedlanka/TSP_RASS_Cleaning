    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {

            int row = jTable1.getSelectedRow();
            String table_click = jTable1.getModel().getValueAt(row, 0).toString();

            Statement st = DbConnection.getconnection().createStatement();
            ResultSet rs = st.executeQuery("select * from emp_atten_admin_staff where EPFno='" + table_click + "' and Month='" + cmb_month.getSelectedItem().toString() + "' and Year='" + cmb_year.getSelectedItem().toString() + "' ");

            while (rs.next()) {

                txt_empid.setText(rs.getString("EPFno"));
                txt_emp_name.setText(rs.getString("Name"));

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jTable1MouseClicked

