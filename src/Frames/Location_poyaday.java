    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (txt_amt.getText().isEmpty() | txt_locName.getText().isEmpty()) {
        } else {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            Vector v = new Vector();
            
            table_data t = new table_data();
            t.poya_rank = cmb_EffectiveRank.getSelectedItem().toString();
            t.poya_Loc = cmb_defLocation.getSelectedItem().toString();
            t.poya_Amount = txt_amt.getText();
            
            al.add(t);
            
            v.add(cmb_defLocation.getSelectedItem().toString());
            v.add(cmb_EffectiveRank.getSelectedItem().toString());
            v.add(txt_amt.getText());
            
            dtm.addRow(v);
            
            txt_amt.setText("");
            
        }

    }//GEN-LAST:event_jButton5ActionPerformed

