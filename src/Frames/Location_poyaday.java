    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        int nrow = jTable1.getModel().getRowCount();
        
        if (nrow == 0) {
            JOptionPane.showMessageDialog(rootPane, " No Data to Save...!");
        } else {
            try {
                
                Statement st = DbConnection.getconnection().createStatement();
                for (table_data t : al) {
                    st.executeUpdate("insert into location_poyaday_rates values ('" + t.poya_Loc + "','" + t.poya_rank + "','" + t.poya_Amount + "')");
                }
                
                JOptionPane.showMessageDialog(rootPane, " Shift Rates Saved...!");
                
                clear();
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
        

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int row = jTable1.getSelectedRow();
        
        int nrow = jTable1.getModel().getRowCount();
        
        if (nrow == 0) {
        } else {
            String loc = jTable1.getModel().getValueAt(row, 0).toString();
            String rank = jTable1.getModel().getValueAt(row, 1).toString();
            String amount = jTable1.getModel().getValueAt(row, 2).toString();
            
            try {
                
                Statement st = DbConnection.getconnection().createStatement();
                st.executeUpdate("delete from location_poyaday_rates where LocCode = '" + loc + "' and Rank ='" + rank + "' and PoyaDayAmt = '" + amount + "'  ");
                
                JOptionPane.showMessageDialog(rootPane, " Selected Data Deleted... ");
                
                dtm.removeRow(row);
                
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

